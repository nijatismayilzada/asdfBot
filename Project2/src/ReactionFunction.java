public class ReactionFunction {
  private float a;
  private float b;

  public ReactionFunction(float a, float b) {
    this.a = a;
    this.b = b;
  }

  public float getA() {
    return a;
  }

  public void setA(float a) {
    this.a = a;
  }

  public float getB() {
    return b;
  }

  public void setB(float b) {
    this.b = b;
  }
}
